    @Override
    public void update(java.util.Observable updatedModel, Object parametros) {
        if (parametros != Cliente_Modelo.ae_usuario_actual) {
            return;
        }
        Cliente usuarioCurrent = modelo.obtener_cliente_actual();
        if (usuarioCurrent == null) {
            this.jTextField_id.setText("");
            this.jTextField_nombre.setText("");
        } else {
            this.jTextField_id.setText(usuarioCurrent.getCodigoC());
            if (modelo.obtener_errores().get("id") != null) {
                this.jTextField_id.setBorder(BORDER_ERROR);
                this.jTextField_id.setToolTipText(modelo.obtener_errores().get("id"));
            } else {
                this.jTextField_id.setBorder(BORDER_NOBORDER);
                this.jTextField_id.setToolTipText(null);
            }
            this.jTextField_nombre.setText(usuarioCurrent.getNombreC());
            if (modelo.obtener_errores().get("nombre") != null) {
                this.jTextField_nombre.setBorder(BORDER_ERROR);
                this.jTextField_nombre.setToolTipText(modelo.obtener_errores().get("nombre"));
            } else {
                this.jTextField_nombre.setBorder(BORDER_NOBORDER);
                this.jTextField_nombre.setToolTipText(null);
            }

        }

        this.validate();
        if (modelo.obtener_mensaje() != null) {
            if (!modelo.obtener_mensaje().equals("")) {
                JOptionPane.showMessageDialog(this, modelo.obtener_mensaje(), "", JOptionPane.INFORMATION_MESSAGE);
                modelo.colocar_mensaje("");
            }
        }
    }
